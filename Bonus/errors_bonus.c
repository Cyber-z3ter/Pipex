#include "pipex_bonus.h"

/* **************************************************** */
/*                   ft_error_bonus                     */
/* **************************************************** */

void	ft_error_bonus(t_bdata *pipex, char *str)
{
	perror(str);
	ft_free_bonus(pipex);
	exit(1);
}

/* **************************************************** */
/*                     msg_error                        */
/* **************************************************** */

int	err_msg_bonus(t_bdata *pipex, char *str)
{
	ft_putstr_fd(str, 2);
	ft_free_bonus(pipex);
	exit(1);
}