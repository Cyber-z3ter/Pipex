void	ft_error(t_data *pipex, char *str)
{
	perror(str);
	ft_free(pipex);
	exit(EXIT_FAILURE);
}

/* **************************************************** */
/*                   🅼🆂🅶_🅴🆁🆁🅾🆁                     */
/* **************************************************** */

int	err_msg(t_data *pipex, char *str)
{
	ft_putstr_fd(str, 2);
	ft_free(pipex);
	exit(EXIT_FAILURE);
}
